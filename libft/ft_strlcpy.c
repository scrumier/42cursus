#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t maxlen)
{
	unsigned long int	srclen;

	srclen = ft_strlen((char *)src);
	if (srclen + 1 < maxlen)
	{
		ft_memcpy(dst, src, srclen + 1);
	}
	else if (maxlen != 0)
	{
		ft_memcpy(dst, src, maxlen - 1);
		dst[maxlen - 1] = '\0';
	}
	return (srclen);
}
